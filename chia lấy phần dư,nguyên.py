a = int(input("Nhập số nguyên dương a: "))
b =int(input("Nhập số nguyên dương b: "))
quotient = a // b 
remainder = a % b
print(f"Kết quả chia lấy phần nguyên của a với b là: {quotient}")
print(f"Kết quả chia lấy phần dư của a với b là: {remainder}")